class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'es';
        this.init();
    }

    init() {
        // Cargar traducciones inmediatamente
        this.applyTranslations();
        
        // Configurar event listeners
        this.setupLanguageSelectors();
        
        // Aplicar idioma guardado
        this.updateLanguageSelectorUI();
    }

    setupLanguageSelectors() {
        // Selector en el header
        const headerSelector = document.getElementById('language-select-header');
        if (headerSelector) {
            headerSelector.addEventListener('change', (e) => {
                this.changeLanguage(e.target.value);
            });
        }

        // Selector en móvil
        const mobileSelector = document.getElementById('language-select');
        if (mobileSelector) {
            mobileSelector.addEventListener('change', (e) => {
                this.changeLanguage(e.target.value);
            });
        }

        // Event listeners para banderas o botones de idioma
        document.querySelectorAll('[data-language]').forEach(element => {
            element.addEventListener('click', (e) => {
                e.preventDefault();
                this.changeLanguage(element.getAttribute('data-language'));
            });
        });
    }

    changeLanguage(language) {
        if (language === 'spanish') language = 'es';
        if (language === 'english') language = 'en';

        this.currentLanguage = language;
        localStorage.setItem('selectedLanguage', language);
        
        // Aplicar traducciones a toda la página
        this.applyTranslations();
        
        // Actualizar UI de selectores
        this.updateLanguageSelectorUI();
    }

    applyTranslations() {
        const lang = this.currentLanguage;
        
        // Traducir elementos con atributo data-translate
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[lang] && translations[lang][key]) {
                element.textContent = translations[lang][key];
            }
        });

        // Traducir atributos placeholder
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (translations[lang] && translations[lang][key]) {
                element.placeholder = translations[lang][key];
            }
        });

        // Traducir atributos title
        document.querySelectorAll('[data-translate-title]').forEach(element => {
            const key = element.getAttribute('data-translate-title');
            if (translations[lang] && translations[lang][key]) {
                element.title = translations[lang][key];
            }
        });

        // Traducir page-header dinámico
        this.translatePageHeader();

        // Actualizar atributo lang del HTML
        document.documentElement.lang = lang;
        
        // Disparar evento personalizado para que otros scripts se enteren del cambio
        const event = new CustomEvent('languageChanged', { detail: { language: lang } });
        document.dispatchEvent(event);
    }

    updateLanguageSelectorUI() {
        const headerSelector = document.getElementById('language-select-header');
        if (headerSelector) {
            headerSelector.value = this.currentLanguage;
        }

        const mobileSelector = document.getElementById('language-select');
        if (mobileSelector) {
            mobileSelector.value = this.currentLanguage;
        }
    }

    getTranslation(key) {
        return translations[this.currentLanguage]?.[key] || key;
    }

    translatePageHeader() {
        const lang = this.currentLanguage;
        const pageTitle = document.getElementById('page-title');
        const breadcrumbTitle = document.getElementById('breadcrumb-title');
        
        if (pageTitle) {
            const currentText = pageTitle.textContent.trim();
            // Mapeo de textos dinámicos a claves de traducción
            const textMap = {
                'Productos': 'productos',
                'Servicios': 'productos',
                'Products': 'productos',
                'Services': 'productos',
                'Quienes Somos': 'quienes_somos',
                'About Us': 'quienes_somos',
                'Contactos': 'contactos',
                'Contactenos': 'contactenos',
                'Contact': 'contactenos'
            };
            
            const key = textMap[currentText];
            if (key && translations[lang][key]) {
                pageTitle.textContent = translations[lang][key];
                if (breadcrumbTitle) {
                    breadcrumbTitle.textContent = translations[lang][key];
                }
            }
        }
    }
}

class ThemeManager {
    constructor() {
        this.currentTheme = localStorage.getItem('selectedTheme') || 'light';
        this.init();
    }

    init() {
        this.applyTheme();
        this.setupThemeToggles();
        this.updateThemeToggleUI();
    }

    setupThemeToggles() {
        document.querySelectorAll('[data-theme-toggle]').forEach((toggle) => {
            toggle.addEventListener('change', (event) => {
                this.changeTheme(event.target.checked ? 'dark' : 'light');
            });
        });
    }

    changeTheme(theme) {
        this.currentTheme = theme;
        localStorage.setItem('selectedTheme', theme);
        this.applyTheme();
        this.updateThemeToggleUI();
    }

    applyTheme() {
        const isDark = this.currentTheme === 'dark';
        document.documentElement.classList.toggle('theme-dark', isDark);
        document.documentElement.classList.toggle('theme-light', !isDark);
        document.documentElement.setAttribute('data-theme', this.currentTheme);
    }

    updateThemeToggleUI() {
        const isDark = this.currentTheme === 'dark';
        document.querySelectorAll('[data-theme-toggle]').forEach((toggle) => {
            toggle.checked = isDark;
        });
    }
}

// Inicializar cuando el DOM esté listo
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        window.languageManager = new LanguageManager();
        window.themeManager = new ThemeManager();
    });
} else {
    window.languageManager = new LanguageManager();
    window.themeManager = new ThemeManager();
}
